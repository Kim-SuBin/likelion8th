# 리스트의 길이를 구해주는 함수 - len(변수)
print(len(li))
# 리스트 원소를 오름차순 정렬해주는 함수 - 변수.sort()
li_2.sort()
print(li_2)
# 리스트 내의 특정 원소 인덱스를 변환해주는 함수 - 변수.index()
print(li.index)
# 리스트 내의 특정 원소의 개수 세기 - .count(특정 원소)
